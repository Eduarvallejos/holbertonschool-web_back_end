#!/usr/bin/env python3
"""Definition of the add function, which takes two parameters 'a' and 'b',
both of type float, and returns their sum as a float."""


def add(a: float, b: float) -> float:
    """
    Add two floating-point numbers.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of the two numbers.
    """
    return a + b
