#!/usr/bin/env python3
"""
We define a function called 'to_str' that takes a floating number 'n'
as input and returns its string representation using the 'str()' function.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of a given float number.

    Parameters:
        number (float): The floating number.

    Returns:
        str: The string representation of the float number.
    """
    return str(n)
