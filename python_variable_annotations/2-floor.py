#!/usr/bin/env python3
"""
This function takes a float as input and
returns its closest minor integer value.
"""
import math


def floor(n: float) -> int:
    """
    Returns the closest minor integer value of the input float.

    Parameters:
        n (float): The input float.

    Returns:
        int: The floor of the input float.
    """
    return math.floor(n)
